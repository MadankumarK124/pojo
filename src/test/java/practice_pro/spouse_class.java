package practice_pro;

public class spouse_class {
	//declarations
	String sname;
	int  snumber;
	//constructor
	public spouse_class(String sname, int snumber) {
		super();
		this.sname = sname;
		this.snumber = snumber;
	}
	//add getters and setters
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getSnumber() {
		return snumber;
	}
	public void setSnumber(int snumber) {
		this.snumber = snumber;
	}
	
	
	
}
